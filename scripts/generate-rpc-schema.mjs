import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");
const protoPath = resolve(repoRoot, "../protos/policy/v1/policy.proto");
const outputPath = resolve(repoRoot, "src/generated/policy-rpc.schema.ts");

const pbjsBinary = join(repoRoot, "node_modules", ".bin", process.platform === "win32" ? "pbjs.cmd" : "pbjs");

const schemaJson = execFileSync(pbjsBinary, ["-t", "json", protoPath], {
  cwd: repoRoot,
  encoding: "utf8",
});

const fileContents = `// Generated by scripts/generate-rpc-schema.mjs. Do not edit by hand.
const schema = ${schemaJson.trim()} as const;

export default schema;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, fileContents);
